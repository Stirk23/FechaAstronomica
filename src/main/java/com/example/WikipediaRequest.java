package com.example;

public class WikipediaRequest {
    private int year;
    private String text;
    private String nasaApod;

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getNasaApod() {
        return nasaApod;
    }

    public void setNasaApod(String nasaApod) {
        this.nasaApod = nasaApod;
    }


}